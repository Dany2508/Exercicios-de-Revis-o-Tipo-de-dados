//Arrays Multidimensionais

//Declare uma matriz matriz com duas linhas e três colunas, preenchendo-a com números.
const matriz = [
    [1, 2, 3],
    [4, 5, 6]
]

console.log(matriz)

//Exiba a matriz no console.
var valor = matriz[1][2]
console.log(valor)

//Acesse e exiba o valor do elemento na segunda linha, terceira coluna da matriz.
matriz[0][1] = 99
console.log(matriz)
